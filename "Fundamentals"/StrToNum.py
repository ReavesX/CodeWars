def string_to_number(s):
    """
    Args: 
        s (string): A number that is taken as a string 
                    ex: "4"
                    
    Returns: 
        int: The string as an integer 
             ex: "4" becomes 4

    Examples:
       >>>  string_to_number("4")
       4
       
       >>> string_to_number("8")
       8
       
       >>> string_to_number("26")
       26
    
    """
    return int(s)
